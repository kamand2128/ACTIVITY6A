# Print "Hello, World!" to the console
print("Hello, World!")

# Declare variables
name = "Daniel"
age = 25

# Print variables
print("Name:", name)
print("Age:", age)

# Basic math operations
a = 10
b = 5
print("Addition:", a + b)
print("Subtraction:", a - b)
print("Multiplication:", a * b)
print("Division:", a / b)

# Conditional statement
if age > 18:
    print("You are an adult.")
else:
    print("You are a minor.")

# Loop example (prints numbers from 1 to 5)
for i in range(1, 6):
    print("Number:", i)

# Function example
def greet(user):
    return "Hello, " + user + "!"

print(greet(name))
